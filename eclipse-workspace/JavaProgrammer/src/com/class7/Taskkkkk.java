package com.class7;

public class Taskkkkk {

	public static void main(String[] args) {
		
		int num=1;
		while(num<=100) {
			System.out.print(num+" ");
			num++;
		}
		
		
		
		for(int a=1; a<=100; a++) {
			System.out.print(a+" ");
		}
		
		System.out.println("----------------------------------------------------------");
		
		for(int b=100; b>=1; b--) {
			System.out.print(b+" ");
		}
		
		System.out.println("---------------------------------------------------------");
		
		for(int h=20; h>=1; h-=2) {
			System.out.print(h+" ");
		}
		
		System.out.println("-----------------------------------------------");
		
		for(int y=20; y<=50; y+=3) {
			System.out.print(y+" ");
		}
		
	}

}
